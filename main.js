$(document).ready(function() {

  // Mouse Enter '#animals'
  $('#animals').mouseenter(function() {
    $(this).css("background-color", "#0000ff");
    $(this).css("width", "50%");
    $(this).css("font-family", "who asks satan");
    $(this).css("color", "black");
    $(this).css("font-size", "20px");
  });

  // Mouse Leave '#animals'
  $('#animals').mouseleave(function() {
    $(this).css("background-color", "#fff");
    $(this).css("width", "5em");
    $(this).css("color", "white");
    $(this).css("font-size", "15px");
  });

  // Mouse Enter '#bohemians'
  $('#bohemians').mouseenter(function() {
    $(this).css("background-color", "#00ff00");
    $(this).css("width", "50%");
    $(this).css("font-family", "who asks satan");
    $(this).css("color", "black");
    $(this).css("font-size", "20px");
  });

  // Mouse Leave '#bohemians'
  $('#bohemians').mouseleave(function() {
    $(this).css("background-color", "#fff");
    $(this).css("width", "5em");
    $(this).css("color", "white");
    $(this).css("font-size", "15px");
  });

    // Mouse Enter '#coven'
  $('#coven').mouseenter(function() {
    $(this).css("background-color", "#ff0000");
    $(this).css("width", "50%");
    $(this).css("font-family", "who asks satan");
    $(this).css("color", "black");
    $(this).css("font-size", "20px");
  });
  
    // Mouse Leave '#coven'
  $('#coven').mouseleave(function() {
    $(this).css("background-color", "#fff");
    $(this).css("width", "5em");
    $(this).css("color", "white");
    $(this).css("font-size", "15px");
  });
});